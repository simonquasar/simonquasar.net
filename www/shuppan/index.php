                <p>
                    Questo libro esamina in dettaglio <b>l’evoluzione dell’editoria e la nascita della stampa giornalistica giapponese dagli anni pre-Meiji fino alla fine del secolo XIX.</b>
					<br><br>Assieme alle numerose controversie legali, avventure commerciali e ai tumulti politici che caratterizzavano l’epoca, vengono tracciati i ruoli importanti di figure come Fukuzawa Yukichi, Fukuchi Gen’ichirō o Edoardo Chiossone. 
					<br>Esplorando le relazioni tra editori, autori, Stato e opinione pubblica, il saggio delinea gli sviluppi tecnologici, il ruolo degli <i>oranda fūsetsugaki</i>, la nascita dei primi <i>shinbun</i> e degli <i>zasshi</i> fino all’avvento dei <i>kisha club</i>.
					<br><b>Un’analisi inedita per comprendere le fondamenta dell’industria editoriale giapponese moderna</b>. 
                </p>
				
                <p>
                    L’<b>introduzione</b> fornisce il contesto storico necessario per comprendere il panorama del Giappone prima del 1853, nonché i rapporti degli eventi internazionali riportati dai mercanti olandesi, ossia gli <i>oranda fūsetsugaki</i>.
                </p>

                <p>
                    Nella parte "<b>Infrastruttura Editoriale</b> 1853–1875", si esamina l'arrivo di un nuovo mondo con l'apertura dei porti giapponesi e la conseguente inondazione di informazioni e idee stranere. Qui si approfondisce il ruolo di figure come Yanagawa Shunsan e le pubblicazioni dello <i>yōsho shirabesho</i>, il primo giornale in lingua autoctona e l'influenza di Fukuzawa Yukichi sul <i>bunmei kaika</i>.
                </p>

                <p>
                    In  "<b>Sviluppo Tecnologico</b> 1590–1900" si esplorano le sfide tipografiche, la distribuzione e la creazione del sistema postale lungo l'arcipelago. Inoltre, discuteremo delle tecniche di abbonamento, delle questioni legate alla carta, e degli impatti delle crisi politiche, come la ribellione di Satsuma, sull'editoria.
                </p>

                <p>
                    Nella sezione "<b>Stato ed Editoria</b> 1868–1890", il saggio descrive i rapporti tra politica ed editoria, il movimento per i diritti del popolo e la nascita dei <i>kisha club</i>.
                </p>
 
                <p>
                    In "<b>Cultura Letteraria</b> 1870-1890" si delinea la formazione dell'opinione pubblica e della cultura "moderna", l'ascesa delle <i>zasshi</i> (riviste), dei Yellow Papers, e l'apparizione di cartelli dell'informazione.
                </p>

                <p>
                    In <b>Appendice</b>, infine, ulteriori dettagli e risorse preziose, inclusi <b>elenchi di pubblicazioni</b>, eventi storici, <b>legislazioni</b> e <b>dati statistici</b>, che offrono un quadro completo e un supporto empirico e statistico alle analisi presentate.
                </p>
 
                
